CREATE TABLE Category (
    ID INT PRIMARY KEY,
    Name VARCHAR(255)
);

CREATE TABLE Location (
    id INT PRIMARY KEY,
    area VARCHAR(255),
    city VARCHAR(255),
    country VARCHAR(255)
);

CREATE TABLE Contact (
    id INT PRIMARY KEY,
    Phone VARCHAR(255),
    Another_phone INT,
    email VARCHAR(255),
    whatsapp INT
);

ALTER TABLE Contact
MODIFY COLUMN Another_phone VARCHAR(225);

ALTER TABLE Contact
MODIFY COLUMN whatsapp VARCHAR(225);

CREATE TABLE Profile (
    id INT PRIMARY KEY,
    Description VARCHAR(255),
    Personal_Image BLOB,
    Contact_ID INT,
    FOREIGN KEY (Contact_ID) REFERENCES Contact(id)
);

CREATE TABLE Admin (
    id INT PRIMARY KEY,
    Username VARCHAR(255),
    Password VARCHAR(255)
);

CREATE TABLE Craftsman (
    id INT PRIMARY KEY,
    Name VARCHAR(255),
    Location_id INT,
    Category_id INT,
    Profile_id INT,
    FOREIGN KEY (Location_id) REFERENCES Location(id),
    FOREIGN KEY (Category_id) REFERENCES Category(ID),
    FOREIGN KEY (Profile_id) REFERENCES Profile(id)
);

